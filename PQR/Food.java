package PQR;

public class Food {
		public static void main(String[] args) {
			String  ch = "A";
			switch(ch) {
			
			case "A":
				System.out.println("Pizza");
				break;
				
			case "B":
				System.out.println("Burger");
				break;
				
			case "C":
				System.out.println("Sandwich");
				break;
				
			case "D":
				System.out.println("Pasta");
				break;
				
				default:
					System.out.println("Invalid");
			}
	}

}
