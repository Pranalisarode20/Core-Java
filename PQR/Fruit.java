package PQR;

public class Fruit {
	public static void main(String[] args) {
		int num = 5;
		switch(num) {
		
		case 1:
			System.out.println("Price of Apple : 100");
			break;
			
		case 2: 
			System.out.println("Price of Banana : 40");
			break;
			
		case 3:
			System.out.println("Price of Mango : 80");
			break;
			
		case 4:
			System.out.println("Price of Orange : 60");
			break;
			
			default:
				System.out.println("Stock Out");
		}
	}
}
