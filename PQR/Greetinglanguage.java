package PQR;

public class Greetinglanguage {
	public static void main(String[] args) {
		String ch = "English";
		 switch(ch) {
		 
		 case "English":
			 System.out.println(" Hello...!");
			 break;
			 
		 case "Hindi":
			 System.out.println(" Namaste...!");
			 break;
			 
		 case "Marathi":
			 System.out.println(" Namaskar...!");
			 break;
			 
			 default:
				 System.out.println("Invalid..");
		 }
	}

}
