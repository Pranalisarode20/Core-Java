package UIK;

public class Language {
	public static void main(String[] args) {
		int num = 4;
		switch(num) {
		
		case 1:
			System.out.println("Java");
			break;
			
		case 2:
			System.out.println("Python");
			break;
			
		case 3:
			System.out.println("C++");
			break;
			
		case 4:
			System.out.println("JavaScript");
			break;
			
			default:
				System.out.println("Invalid Language Code");
		}
	}

}
