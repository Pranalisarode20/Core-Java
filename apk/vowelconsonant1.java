package apk;

public class vowelconsonant1 {
	public static void main(String[] args) {
		char vowel = 'a';
		if (vowel == 'a' || vowel == 'e' || vowel == 'i' || vowel == 'o' || vowel == 'u') {
			System.out.println("character is vowel");
		} else {
			System.out.println("character is consonant");
		}
	}

}
